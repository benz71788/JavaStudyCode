package ex4_10_incre_dec;

public class IncreDecExample2 {
	
	public static void main(String[] args) {
		
		int n1 = 10, n2 = 10, n3 = 10, n4 = 10;
		
		System.out.println(++n1);	// 11
		
		System.out.println(n1);		// 11
		
		System.out.println(n2++);	// 10
		
		System.out.println(n2);		// 11
	}

}
